package array;

/**
 * An array is a data structure that stores a collection of elements of the same data type 
 * in a contiguous block of memory. It has fixed size so cannot be modified once created.
 * 
 */
public class DSA_Array {

	public static void main(String[] args) {

		// Declare an arry, here we created an array of size 5, by default all elements
		// are stored as 0 in int Type
		int[] array = new int[5];

		// insert an element in array
		array[0] = 3;

		// intializing at creation time
		int[] intializedArray = { 1, 2, 3 };

	}

}
